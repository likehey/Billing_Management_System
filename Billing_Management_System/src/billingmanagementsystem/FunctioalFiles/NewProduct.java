    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
        // TODO add your handling code here:
        String pid=jLabel4.getText();
        String pname=jTextField1.getText();
        String rate=jTextField2.getText();
        String desc=jTextField3.getText();
        String qt=quantity.getValue().toString();
        int quant=Integer.parseInt(qt);
        String activate=(String)jComboBox1.getSelectedItem();
        if(qt.isEmpty()|| qt.contentEquals("0"))
        {
            i=false;

            JOptionPane.showMessageDialog(null,"Please fill the quatity field");
            setVisible(false);
            new NewProduct().setVisible(true);
        }
        if(pid.isEmpty())
        {
            i=false;

            JOptionPane.showMessageDialog(null,"Please fill the id field");
            setVisible(false);
            new NewProduct().setVisible(true);
        }

        if(pname.isEmpty()|| (pname.contains("Enter the product Name")) )
        {
            i=false;
            JOptionPane.showMessageDialog(null,"Please fill the product name  field");
            setVisible(false);
            new NewProduct().setVisible(true);

        }

        if(rate.isEmpty()|| (rate.contains("Enter the Rate")) )
        {
            i=false;
            JOptionPane.showMessageDialog(null,"Please fill the Rate field");
            setVisible(false);
            new NewProduct().setVisible(true);

        }
        if((desc.contains("Enter the product description")) )
        {
            i=false;
            JOptionPane.showMessageDialog(null,"Please fill the valid description in description field");
            setVisible(false);
            new NewProduct().setVisible(true);

        }
        if(i=true){
            try{
                Connection con=Connections.getcon();
                Statement st=con.createStatement();
                st.executeUpdate("INSERT INTO `product`(`PID`, `PName`, `PRate`, `PDescription`, `Pquantity`, `Activate`) VALUES"
                    + "('"+pid+"','"+pname+"','"+rate+"','"+desc+"','"+quant+"','"+activate+"')");
                JOptionPane.showMessageDialog(null, "successfully inserted!");
                con.close();

                setVisible(false);
                new NewProduct().setVisible(true);
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,"The product name or id already registered !");
            }
        }
    }//GEN-LAST:event_SaveButtonActionPerformed

